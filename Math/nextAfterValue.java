package Mathematics;

public class nextAfterValue {
    public static void main(String[] args) {
        System.out.println("the next after value of 10 in direction of 7 is : "+Math.nextAfter(9, 6));
        System.out.println("the next after value of 11 in direction of 5 is : "+Math.nextAfter(4, 2));
    }
}
